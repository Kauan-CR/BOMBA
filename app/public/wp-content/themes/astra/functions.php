function painel_arquivos_completo_shortcode() {
    if (!is_user_logged_in()) {
        return '<p>Por favor, fa√ßa login para acessar esta p√°gina.</p>';
    }

    $output = '';

    // Processa upload se for POST e arquivo enviado
    if ($_SERVER['REQUEST_METHOD'] === 'POST' && !empty($_FILES['arquivo_upload'])) {
        require_once(ABSPATH . 'wp-admin/includes/file.php');
        require_once(ABSPATH . 'wp-admin/includes/image.php');
        require_once(ABSPATH . 'wp-admin/includes/media.php');

        $uploadedfile = $_FILES['arquivo_upload'];
        $upload_overrides = array('test_form' => false);

        $movefile = wp_handle_upload($uploadedfile, $upload_overrides);

        if ($movefile && !isset($movefile['error'])) {
            $attachment = array(
                'guid'           => $movefile['url'],
                'post_mime_type' => $movefile['type'],
                'post_title'     => sanitize_file_name($uploadedfile['name']),
                'post_content'   => '',
                'post_status'    => 'inherit',
                'post_author'    => get_current_user_id(),
            );

            $attach_id = wp_insert_attachment($attachment, $movefile['file']);
            $attach_data = wp_generate_attachment_metadata($attach_id, $movefile['file']);
            wp_update_attachment_metadata($attach_id, $attach_data);

            $output .= '<p style="color:green;">Upload realizado com sucesso!</p>';
        } else {
            $output .= '<p style="color:red;">Erro no upload: ' . esc_html($movefile['error']) . '</p>';
        }
    }

    // Formul√°rio de upload
    $output .= '
    <form method="post" enctype="multipart/form-data" style="margin-bottom: 2em;">
        <label for="arquivo_upload">Enviar arquivo:</label><br>
        <input type="file" name="arquivo_upload" id="arquivo_upload" required><br><br>
        <button type="submit">Enviar</button>
    </form>';

    // Lista arquivos
    $current_user_id = get_current_user_id();

    if ( current_user_can('administrator') ) {
        $args = array(
            'post_type' => 'attachment',
            'posts_per_page' => -1,
            'post_status' => 'inherit',
            'orderby' => 'date',
            'order' => 'DESC',
        );
    } else {
        $args = array(
            'post_type' => 'attachment',
            'author' => $current_user_id,
            'posts_per_page' => -1,
            'post_status' => 'inherit',
            'orderby' => 'date',
            'order' => 'DESC',
        );
    }

    $query = new WP_Query($args);

    if ( !$query->have_posts() ) {
        $output .= '<p>Nenhum arquivo encontrado.</p>';
    } else {
        $output .= '<h3>Arquivos dispon√≠veis:</h3><ul>';
        while ( $query->have_posts() ) {
            $query->the_post();
            $url = wp_get_attachment_url(get_the_ID());
            $title = get_the_title();
            $author_id = get_post_field('post_author', get_the_ID());
            $author_name = get_the_author_meta('display_name', $author_id);
            $date = get_the_date('d/m/Y H:i');

            $output .= '<li><a href="' . esc_url($url) . '" target="_blank" rel="noopener noreferrer">' . esc_html($title) . '</a> ‚Äî enviado por <strong>' . esc_html($author_name) . '</strong> em ' . esc_html($date) . '</li>';
        }
        $output .= '</ul>';
        wp_reset_postdata();
    }

    return $output;
}
add_shortcode('painel_arquivos', 'painel_arquivos_completo_shortcode');

